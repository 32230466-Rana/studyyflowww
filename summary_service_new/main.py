def strip_page_reference_from_question(question: str) -> str:
    question = clean_quiz_part(question)
    if not question:
        return ""

    question = re.sub(
        r"\s*\((?:according to|as described on|as shown on|based on)\s+page\s+\d+\s*(?:of the source|of the source material)?\)",
        "",
        question,
        flags=re.IGNORECASE,
    )
    question = re.sub(
        r"\s*(?:,?\s*)?(?:according to|as described on|as shown on|based on)\s+page\s+\d+\s*(?:of the source|of the source material)?",
        "",
        question,
        flags=re.IGNORECASE,
    )
    question = re.sub(
        r"\s*(?:,?\s*)?(?:according to|as described in|based on)\s+(?:the\s+)?(?:source|source material)",
        "",
        question,
        flags=re.IGNORECASE,
    )
    question = re.sub(r"\s+", " ", question).strip()
    question = re.sub(r"\s+\?", "?", question).strip()

    if question and not question.endswith("?"):
        question = question.rstrip(" .") + "?"

    return question


