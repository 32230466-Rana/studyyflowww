def split_text_into_chunks(text: str, max_chars: int = 1600) -> list[str]:
    text = clean_source_text(text)
    if not text:
        return []

    page_blocks = re.split(r"(?=---\s*PAGE\s*\d+\s*---)", text, flags=re.IGNORECASE)
    if len(page_blocks) <= 1:
        page_blocks = re.split(r"\n\s*\n+", text)

    chunks: list[str] = []
    current = ""

    for block in page_blocks:
        block = block.strip()
        if not block:
            continue

        if len(current) + len(block) + 2 <= max_chars:
            current += block + "\n\n"
        else:
            if current.strip():
                chunks.append(current.strip())

            if len(block) > max_chars:
                for i in range(0, len(block), max_chars):
                    part = block[i:i + max_chars].strip()
                    if part:
                        chunks.append(part)
                current = ""
            else:
                current = block + "\n\n"

    if current.strip():
        chunks.append(current.strip())

    return chunks


def words_key(text: str) -> set[str]:
    stop_words = {
        "the", "a", "an", "and", "or", "of", "to", "in", "on", "for", "with",
        "is", "are", "was", "were", "as", "that", "which", "what", "when",
        "does", "do", "from", "into", "by", "it", "they", "them", "this",
        "these", "those", "be", "been", "being", "can", "could", "would",
        "should", "must", "may", "might", "will", "shall", "has", "have",
        "had", "not", "but", "than", "then", "also", "only", "such", "more",
        "most", "less", "between", "among", "through", "about", "their",
        "there", "where", "while", "during", "because", "therefore", "according",
        "page", "slide", "source", "text", "document", "material",
    }

    words = re.findall(r"[a-zA-Z][a-zA-Z0-9_-]+", str(text).lower())
    return {w for w in words if w not in stop_words and len(w) > 2}


def text_similarity(text1: str, text2: str) -> float:
    w1 = words_key(text1)
    w2 = words_key(text2)
    if not w1 or not w2:
        return 0.0
    return len(w1 & w2) / max(1, len(w1 | w2))


def source_support_score(answer_text: str, source_context: str) -> float:
    answer_words = words_key(answer_text)
    source_words = words_key(source_context)
    if not answer_words or not source_words:
        return 0.0
    return len(answer_words & source_words) / max(1, len(answer_words))


def score_context_chunk(chunk: str) -> int:
    low = chunk.lower()
    words = re.findall(r"[a-zA-Z][a-zA-Z0-9_-]+", chunk)
    unique_words = set(w.lower() for w in words)

    score = 0

    if 450 <= len(chunk) <= 1800:
        score += 8
    elif 250 <= len(chunk) < 450:
        score += 4
    elif len(chunk) > 2400:
        score -= 4

    if len(unique_words) >= 50:
        score += 5
    elif len(unique_words) >= 30:
        score += 3

    useful_markers = [
        "definition", "defined as", "refers to", "means", "type", "types",
        "category", "categories", "process", "steps", "stage", "stages",
        "relationship", "related", "compare", "contrast", "difference",
        "cause", "effect", "result", "consequence", "advantage", "disadvantage",
        "benefit", "limitation", "example", "case", "method", "model",
        "concept", "important", "used to", "based on", "consists of",
        "diagram", "event", "response", "flow", "data flow",
    ]

    for marker in useful_markers:
        if marker in low:
            score += 2

    bad_markers = ["table of contents", "references", "bibliography", "copyright", "all rights reserved"]
    for marker in bad_markers:
        if marker in low:
            score -= 8

    symbol_count = len(re.findall(r"[{}|_=<>]", chunk))
    if symbol_count > 25:
        score -= 5

    return score


