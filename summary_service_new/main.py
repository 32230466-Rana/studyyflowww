def combine_summaries(summaries: list[str]) -> str:
    if not summaries:
        return "No summary could be generated."

    if len(summaries) == 1:
        return summaries[0]

    joined = "\n\n".join(summaries)

    prompt = f"""
You are a careful academic summarization assistant.

TASK:
Combine the section summaries into one clean study summary.

RULES:
- Use ONLY the section summaries.
- Do NOT use outside knowledge.
- Do NOT invent new facts.
- Remove repetition.
- Keep the answer in English.
- Make it useful for exam revision.

FINAL OUTPUT FORMAT:
# Text Summary

## 1. Main Idea
Write 2-3 sentences.

## 2. Main Topics
- ...

## 3. Key Facts and Concepts
- ...

## 4. Important Details
- ...

## 5. Exam Revision Points
- ...

## 6. Short Final Summary
Write one short paragraph.

SECTION SUMMARIES:
{joined}
"""

    return call_ollama_generate(
        prompt=prompt,
        model=SUMMARY_MODEL,
        format_json=False,
        timeout=600,
        num_ctx=4096,
        num_predict=1200,
        temperature=0.1,
        top_p=0.85,
        repeat_penalty=1.10,
    )


def summarize_uploaded_text(text: str) -> str:
    text = clean_source_text(text)

    if len(text) < 20:
        return "Please enter enough text to summarize."

    if len(text) <= 3000:
        max_chars = SUMMARY_CHUNK_CHARS_SMALL
    elif len(text) <= 9000:
        max_chars = SUMMARY_CHUNK_CHARS_MEDIUM
    else:
        max_chars = SUMMARY_CHUNK_CHARS_LARGE

    chunks = split_text_into_chunks(text, max_chars=max_chars)

    if not chunks:
        return "Please enter enough text to summarize."

    partial_summaries: list[str] = []
    for index, chunk in enumerate(chunks, start=1):
        logger.info(f"Summarizing chunk {index}/{len(chunks)}")
        partial_summaries.append(summarize_chunk(chunk, index, len(chunks)))

    return combine_summaries(partial_summaries)


