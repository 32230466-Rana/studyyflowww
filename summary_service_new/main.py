def normalize_match_text(text: str) -> str:
    text = clean_quiz_part(text).lower()
    text = re.sub(r"[^a-z0-9\s\-_/]", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


def term_match_score(term: str, answer_text: str) -> float:
    term_norm = normalize_match_text(term)
    answer_norm = normalize_match_text(answer_text)

    if not term_norm or not answer_norm:
        return 0.0
    if term_norm in answer_norm:
        return 1.0

    term_words = words_key(term_norm)
    answer_words = words_key(answer_norm)
    if not term_words or not answer_words:
        return 0.0
    return len(term_words & answer_words) / max(1, len(term_words))


def split_user_sentences(answer: str) -> list[str]:
    answer = clean_quiz_part(answer)
    if not answer:
        return []
    parts = re.split(r"(?<=[.!?])\s+|\n+", answer)
    sentences = [clean_quiz_part(part) for part in parts if clean_quiz_part(part)]
    return sentences or [answer]


def sentence_correctness(sentence: str, expected_answer: str, explanation: str) -> SentenceFeedback:
    expected_similarity = text_similarity(sentence, expected_answer)
    expected_support = source_support_score(sentence, expected_answer)
    explanation_similarity = text_similarity(sentence, explanation) if explanation else 0.0
    explanation_support = source_support_score(sentence, explanation) if explanation else 0.0

    confidence = max(expected_similarity, expected_support, explanation_similarity, explanation_support)
    is_correct = expected_similarity >= 0.18 or expected_support >= 0.35 or explanation_similarity >= 0.18 or explanation_support >= 0.35

    return SentenceFeedback(
        sentence=sentence,
        is_correct=is_correct,
        confidence=round(float(min(confidence, 1.0)), 2),
        matched_keywords=[],
        comment="Supported by the expected answer." if is_correct else "Not clearly supported by the expected answer. Check this sentence.",
    )


def grade_one_written_answer(item: WrittenUserAnswer) -> WrittenAnswerGrade:
    user_answer = clean_quiz_part(item.user_answer)
    expected_answer = clean_quiz_part(item.expected_answer)
    explanation = clean_quiz_part(item.explanation or "")

    if not user_answer:
        return WrittenAnswerGrade(
            question=item.question,
            user_answer=user_answer,
            score=0.0,
            status="No answer",
            matched_keywords=[],
            missing_keywords=[],
            matched_points=[],
            missing_points=[],
            sentence_feedback=[],
            expected_answer=expected_answer,
            explanation=explanation,
            page=item.page,
        )

    expected_similarity = text_similarity(user_answer, expected_answer)
    expected_support = source_support_score(user_answer, expected_answer)
    explanation_similarity = text_similarity(user_answer, explanation) if explanation else 0.0
    explanation_support = source_support_score(user_answer, explanation) if explanation else 0.0

    score = (
        expected_similarity * 4.0
        + expected_support * 3.5
        + explanation_similarity * 1.2
        + explanation_support * 1.3
    )

    if len(words_key(user_answer)) >= 6:
        score += 0.5

    score = round(min(10.0, score), 1)

    if score >= 8:
        status = "Correct"
    elif score >= 5:
        status = "Partially correct"
    else:
        status = "Needs review"

    feedback = [
        sentence_correctness(sentence, expected_answer, explanation)
        for sentence in split_user_sentences(user_answer)
    ]

    return WrittenAnswerGrade(
        question=item.question,
        user_answer=user_answer,
        score=score,
        status=status,
        matched_keywords=[],
        missing_keywords=[],
        matched_points=[],
        missing_points=[],
        sentence_feedback=feedback,
        expected_answer=expected_answer,
        explanation=explanation,
        page=item.page,
    )


def grade_written_answers(answers: list[WrittenUserAnswer]) -> WrittenGradeOutput:
    results = [grade_one_written_answer(item) for item in answers]
    total = round(sum(item.score for item in results), 1)
    percentage = round((total / max(1, len(results) * 10)) * 100, 1)
    return WrittenGradeOutput(total_score=total, percentage=percentage, results=results)


