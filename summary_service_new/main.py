def split_lines_with_pages(text: str) -> list[dict]:
    text = clean_source_text(text)
    rows: list[dict] = []
    current_page: Optional[str] = None

    for raw_line in text.splitlines():
        line = raw_line.strip()
        if not line:
            continue

        page_match = re.fullmatch(r"---\s*PAGE\s*(\d+)\s*---", line, flags=re.IGNORECASE)
        if page_match:
            current_page = page_match.group(1)
            continue

        cleaned = clean_quiz_part(line)
        if 20 <= len(cleaned) <= 280:
            rows.append({"page": current_page, "text": cleaned, "low": cleaned.lower()})

    return rows


def infer_seed_type(line: str) -> tuple[Optional[str], int]:
    low = line.lower()
    score = 0
    seed_type: Optional[str] = None

    comparison_markers = [
        "differ", "different", "compare", "compared", "contrast", "whereas", "while", "unlike", "similar", "versus",
    ]
    relation_markers = [
        "relationship", "relates to", "related to", "connected to", "depends on", "leads to", "results in",
        "because", "therefore", "cause", "effect", "consequence", "used to", "based on", "consists of",
        "creates", "produces", "requires", "must have", "response",
    ]
    process_markers = [
        "first", "next", "then", "after", "before", "step", "process", "merge", "split", "partition",
        "sequence", "create", "draw", "establish", "define", "develop", "list",
    ]
    concept_markers = [
        "definition", "defined as", "refers to", "means", "type", "types", "category", "model", "method",
        "diagram", "data flow", "system", "subsystem", "event", "store", "entity",
    ]

    if any(m in low for m in comparison_markers):
        seed_type = "comparison"
        score += 14
    if any(m in low for m in relation_markers):
        seed_type = seed_type or "relationship"
        score += 12
    if any(m in low for m in process_markers):
        seed_type = seed_type or "process"
        score += 10
    if any(m in low for m in concept_markers):
        seed_type = seed_type or "concept"
        score += 6
    if re.match(r"^\d+\s*[\.)]?\s*[A-Za-z]", line):
        seed_type = "process_step"
        score += 12
    if len(line.split()) >= 6:
        score += 2

    return seed_type, score


def extract_nlp_question_seeds(text: str, max_seeds: int = 16) -> list[dict]:
    rows = split_lines_with_pages(text)
    seeds: list[dict] = []

    for row in rows:
        seed_type, score = infer_seed_type(row["text"])
        if not seed_type:
            continue
        seeds.append({
            "type": seed_type,
            "page": row["page"],
            "text": row["text"],
            "score": score,
        })

    seeds.sort(key=lambda x: x["score"], reverse=True)

    unique: list[dict] = []
    for seed in seeds:
        if any(text_similarity(seed["text"], old["text"]) >= 0.72 for old in unique):
            continue
        unique.append(seed)
        if len(unique) >= max_seeds:
            break

    return unique


def select_quiz_context_from_seeds(text: str, seeds: list[dict], max_chars: int = QUIZ_MAX_CONTEXT_CHARS) -> str:
    chunks = split_text_into_chunks(text, max_chars=1800)

    seed_words: set[str] = set()
    for seed in seeds:
        seed_words |= words_key(seed.get("text", ""))

    scored_chunks: list[tuple[int, int, str]] = []
    for index, chunk in enumerate(chunks):
        score = score_context_chunk(chunk)
        chunk_words = words_key(chunk)
        if seed_words and chunk_words:
            score += int(20 * (len(seed_words & chunk_words) / max(1, len(seed_words))))
        scored_chunks.append((score, index, chunk))

    scored_chunks.sort(key=lambda x: x[0], reverse=True)

    seed_text = "\n".join(
        f"- Type: {s['type']} | Page: {s['page'] or 'unknown'} | Text: {s['text']}"
        for s in seeds[:10]
    )

    selected_parts = ["QUESTION SEEDS:\n" + (seed_text if seed_text else "No strong seeds found.")]
    total = len(selected_parts[0])

    for score, index, chunk in scored_chunks:
        if total + len(chunk) + 20 > max_chars:
            continue
        selected_parts.append(f"\nSOURCE CHUNK {index + 1}:\n{chunk}")
        total += len(chunk) + 20
        if total >= max_chars:
            break

    return "\n\n".join(selected_parts)[:max_chars]


def retrieve_quiz_context(text: str, difficulty: str = "Mixed", max_chars: int = QUIZ_MAX_CONTEXT_CHARS) -> str:
    """
    Local fallback retriever.
    This replaces app.rag_retriever dependency so this file works alone.
    It does NOT generate questions. It only picks useful source context.
    """
    seeds = extract_nlp_question_seeds(text, max_seeds=10)
    return select_quiz_context_from_seeds(text=text, seeds=seeds, max_chars=max_chars)


