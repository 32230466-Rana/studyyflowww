def generate_quiz_from_text(text: str, number_of_questions: int = 5, difficulty: str = "Medium") -> tuple[list[QuizQuestion], str]:
    text = clean_source_text(text)
    if len(text) < 100:
        return [], "Not enough readable text."

    requested_questions = clamp_question_count(number_of_questions)
    difficulty = normalize_difficulty(difficulty)

    seeds = extract_nlp_question_seeds(text, max_seeds=8)
    context = retrieve_quiz_context(text=text, difficulty=difficulty, max_chars=QUIZ_MAX_CONTEXT_CHARS)

    prompt = build_nlp_guided_quiz_prompt(context=context, requested_questions=requested_questions, difficulty=difficulty)
    raw_output = call_quiz_model(prompt)

    candidates = parse_quiz_json(raw_output=raw_output, source_context=context, limit=requested_questions, strict=True)

    if len(candidates) < requested_questions:
        relaxed = parse_quiz_json(raw_output=raw_output, source_context=context, limit=requested_questions, strict=False)
        merged = candidates[:]
        existing_qs = [q for _, q in merged]
        for score, q in relaxed:
            if len(merged) >= requested_questions:
                break
            if not is_duplicate_question(q, existing_qs):
                merged.append((score, q))
                existing_qs.append(q)
        merged.sort(key=lambda item: item[0], reverse=True)
        candidates = merged[:requested_questions]

    questions = select_final_mixed_questions(candidates=candidates, requested_questions=requested_questions)

    repair_raw_output = ""
    repair_call_used = False

    if len(questions) == 0 and '"questions"' in raw_output:
        repair_prompt = build_failure_repair_prompt(
            raw_output=raw_output,
            context=context,
            requested_questions=requested_questions,
            difficulty=difficulty,
        )
        repair_raw_output = call_failure_repair_model(repair_prompt)
        repair_call_used = True
        repair_candidates = parse_quiz_json(
            raw_output=repair_raw_output,
            source_context=context,
            limit=requested_questions,
            strict=False,
        )
        questions = select_final_mixed_questions(
            candidates=repair_candidates,
            requested_questions=requested_questions,
        )

    final_relation_count = sum(1 for q in questions if is_relation_or_comparison_question(q.question))
    hard_style_count = sum(1 for q in questions if is_advanced_style_question(q.question))

    debug_output = f"""
--- STUDYFLOW PHI4-MINI MCQ QUIZ PIPELINE ---
model: {QUIZ_MODEL}
difficulty: {display_difficulty(difficulty)}
difficulty_internal: {difficulty}
requested_questions: {requested_questions}
context_chars: {len(context)}
seeds_extracted: {len(seeds)}
generated_valid_questions: {len(questions)}
hard_style_questions: {hard_style_count}
relation_or_comparison_questions: {final_relation_count}
llama_is_main_generator: true
nlp_used_for_seed_selection_only: true
rule_based_question_generation: false
repair_call_used: {repair_call_used}
llm_calls: {2 if repair_call_used else 1}
num_ctx: {QUIZ_NUM_CTX}
num_predict: {QUIZ_NUM_PREDICT}
question_kinds: {[infer_question_kind(q.question) for q in questions]}

--- QUESTION SEEDS ---
{json.dumps(seeds, ensure_ascii=False, indent=2)}

--- RAW MODEL OUTPUT ---
{raw_output}

--- REPAIR RAW MODEL OUTPUT ---
{repair_raw_output}
""".strip()

    if len(questions) < requested_questions:
        debug_output += f"\n\n--- QUALITY WARNING ---\nOnly {len(questions)} valid questions were generated out of {requested_questions}."

    return questions[:requested_questions], debug_output


