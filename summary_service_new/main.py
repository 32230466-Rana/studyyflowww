def get_answer_text(correct_letter: str, a: str, b: str, c: str, d: str) -> str:
    return {"A": a, "B": b, "C": c, "D": d}.get((correct_letter or "").upper(), "")


def normalize_correct_answer(value: str) -> str:
    value = clean_quiz_part(value).upper()
    if value in ["A", "B", "C", "D"]:
        return value
    match = re.search(r"\b([ABCD])\b", value)
    if match:
        return match.group(1)
    return ""


def extract_question_items_from_broken_json(raw_output: str) -> list[dict]:
    raw_output = clean_model_output(raw_output)

    q_pos = raw_output.find('"questions"')
    if q_pos == -1:
        q_pos = raw_output.find("'questions'")
    if q_pos != -1:
        raw_output = raw_output[q_pos:]

    objects: list[str] = []
    start: Optional[int] = None
    depth = 0
    in_string = False
    escape = False

    for i, ch in enumerate(raw_output):
        if in_string:
            if escape:
                escape = False
            elif ch == "\\":
                escape = True
            elif ch == '"':
                in_string = False
            continue

        if ch == '"':
            in_string = True
            continue
        if ch == "{":
            if depth == 0:
                start = i
            depth += 1
        elif ch == "}":
            if depth > 0:
                depth -= 1
                if depth == 0 and start is not None:
                    objects.append(raw_output[start:i + 1])
                    start = None

    items: list[dict] = []
    for obj in objects:
        if '"question"' not in obj and "'question'" not in obj:
            continue
        cleaned = re.sub(r",\s*}", "}", obj.strip())
        cleaned = re.sub(r",\s*]", "]", cleaned)
        try:
            parsed = json.loads(cleaned)
            if isinstance(parsed, dict):
                items.append(parsed)
        except Exception:
            continue

    return items


def extract_json_from_model_output(raw_output: str) -> Any:
    raw_output = clean_model_output(raw_output)

    try:
        return json.loads(raw_output)
    except Exception:
        pass

    object_match = re.search(r"\{.*\}", raw_output, flags=re.DOTALL)
    if object_match:
        try:
            return json.loads(object_match.group(0))
        except Exception:
            pass

    array_match = re.search(r"\[.*\]", raw_output, flags=re.DOTALL)
    if array_match:
        try:
            return json.loads(array_match.group(0))
        except Exception:
            pass

    salvaged = extract_question_items_from_broken_json(raw_output)
    if salvaged:
        return {"questions": salvaged}

    return None


def contains_bad_source_reference(text: str) -> bool:
    low = clean_quiz_part(text).lower()
    bad_parts = [
        "not explicitly stated", "not stated", "not enough information", "the source does not mention",
        "the document does not say", "implied by the source",
    ]
    return any(part in low for part in bad_parts)


def is_numeric_order_option(text: str) -> bool:
    text = clean_quiz_part(text)
    return bool(re.fullmatch(r"[0-9,\s\-]+", text))


def has_bad_option(options: list[str]) -> bool:
    bad_option_parts = [
        "all of the above", "none of the above", "both a and b", "both a & b", "both are correct",
        "neither a nor b", "neither are correct", "neither", "not enough information",
        "not supported by the source", "invalid option", "unrelated idea", "the system crashes",
        "the document does not say",
    ]

    for option in options:
        low = option.lower()
        if any(part in low for part in bad_option_parts):
            return True
        if is_numeric_order_option(option):
            return True
    return False


def has_partial_correct_option(correct_text: str, wrong_options: list[str]) -> bool:
    correct_words = words_key(correct_text)
    if not correct_words:
        return False

    for option in wrong_options:
        option_words = words_key(option)
        if len(option_words) < 2:
            continue
        overlap = len(option_words & correct_words) / max(1, len(option_words))
        if overlap >= 0.88:
            return True

    return False


def answer_explanation_overlap(correct_text: str, explanation: str) -> float:
    correct_words = words_key(correct_text)
    explanation_words = words_key(explanation)
    if not correct_words or not explanation_words:
        return 0.0
    return len(correct_words & explanation_words) / max(1, len(correct_words))


def find_best_page_for_answer(correct_text: str, source_context: str) -> Optional[str]:
    best_page = None
    best_score = 0.0
    current_page = None

    for raw_line in str(source_context or "").splitlines():
        line = raw_line.strip()
        if not line:
            continue

        seed_page = re.search(r"\bPage:\s*(\d+)\b", line, flags=re.IGNORECASE)
        if seed_page:
            page = seed_page.group(1)
            score = source_support_score(correct_text, line)
            if score > best_score:
                best_score = score
                best_page = page
            continue

        marker = re.fullmatch(r"---\s*PAGE\s*(\d+)\s*---", line, flags=re.IGNORECASE)
        if marker:
            current_page = marker.group(1)
            continue

        if current_page:
            score = source_support_score(correct_text, line)
            if score > best_score:
                best_score = score
                best_page = current_page

    if best_score >= 0.20:
        return best_page
    return None


def normalize_explanation_for_answer(correct_text: str, explanation: str, source_context: str) -> str:
    explanation = clean_quiz_part(explanation)
    correct_text = clean_quiz_part(correct_text)

    if not correct_text:
        return explanation or "The correct answer is supported by the source material."

    overlap = answer_explanation_overlap(correct_text, explanation)
    if explanation and overlap >= 0.28 and not contains_bad_source_reference(explanation):
        return explanation

    page = find_best_page_for_answer(correct_text, source_context)
    if page:
        return f"According to page {page}, {correct_text}."
    return f"According to the source material, {correct_text}."


def is_weak_hard_question(question: str) -> bool:
    q = clean_quiz_part(question).lower()
    weak_patterns = [
        "what is the purpose", "what purpose", "main purpose", "primary purpose",
        "which diagram is used", "which type of data flow", "which type of", "what type of",
        "which diagram", "what diagram", "what is a ", "what is an ", "what are ",
    ]
    if any(pattern in q for pattern in weak_patterns):
        return True
    weak_starts = ["what is ", "what are ", "which type ", "what type ", "which diagram "]
    return any(q.startswith(start) for start in weak_starts)


def infer_question_kind(question: str) -> str:
    q = clean_quiz_part(question).lower()

    if any(x in q for x in ["differ", "different", "compare", "compares", "contrast", "whereas", "while"]):
        return "comparison"
    if any(x in q for x in ["relationship", "relate", "relates", "connection", "connected", "best explains"]):
        return "relationship"
    if any(x in q for x in ["sequence", "order", "progression", "step", "after", "before"]):
        return "sequence"
    if any(x in q for x in ["cause", "effect", "consequence", "result", "leads to", "follows"]):
        return "cause_effect"
    if any(x in q for x in ["apply", "scenario", "best fits", "most likely"]):
        return "application"
    if any(x in q for x in ["best explanation", "best describes", "best accounts", "most accurate"]):
        return "best_explanation"
    return "understanding"


def is_advanced_style_question(question: str) -> bool:
    if is_weak_hard_question(question):
        return False
    return infer_question_kind(question) in [
        "comparison", "relationship", "sequence", "cause_effect", "application", "best_explanation",
    ]


def is_relation_or_comparison_question(question: str) -> bool:
    return infer_question_kind(question) in ["comparison", "relationship", "cause_effect", "best_explanation"]


def question_quality_score(question: str, correct_text: str, explanation: str, support: float) -> int:
    q = clean_quiz_part(question).lower()
    kind = infer_question_kind(question)
    score = 0

    if kind in ["comparison", "relationship", "sequence", "cause_effect", "application", "best_explanation"]:
        score += 10
    if is_relation_or_comparison_question(question):
        score += 8
    if "best explains" in q or "best describes" in q or "best explanation" in q or "most accurate" in q:
        score += 5
    if "differ" in q or "compare" in q or "relationship" in q:
        score += 5
    if support >= 0.45:
        score += 5
    elif support >= 0.30:
        score += 3
    elif support >= 0.12:
        score += 1
    if len(question.split()) >= 8:
        score += 2
    if len(correct_text.split()) >= 4:
        score += 2
    if len(explanation.split()) >= 8:
        score += 2
    if is_weak_hard_question(question):
        score -= 12
    if contains_bad_source_reference(f"{question} {correct_text} {explanation}"):
        score -= 15
    if len(question) < 22:
        score -= 4

    return score


def concept_fingerprint(text: str) -> set[str]:
    words = words_key(text)
    focused = {w for w in words if len(w) >= 6}
    return focused if focused else words


def concept_overlap(text1: str, text2: str) -> float:
    f1 = concept_fingerprint(text1)
    f2 = concept_fingerprint(text2)
    if not f1 or not f2:
        return 0.0
    return len(f1 & f2) / max(1, min(len(f1), len(f2)))


def is_duplicate_question(new_q: QuizQuestion, existing: list[QuizQuestion]) -> bool:
    new_answer = clean_quiz_part(new_q.correct_answer_text)
    new_explanation = clean_quiz_part(new_q.explanation)

    for old in existing:
        old_answer = clean_quiz_part(old.correct_answer_text)
        old_explanation = clean_quiz_part(old.explanation)

        q_sim = text_similarity(new_q.question, old.question)
        ans_sim = text_similarity(new_answer, old_answer)
        exp_sim = text_similarity(new_explanation, old_explanation)

        if q_sim >= 0.84:
            return True
        if ans_sim >= 0.88:
            return True
        if exp_sim >= 0.90:
            return True
        if concept_overlap(new_answer, old_answer) >= 0.86:
            return True
        if concept_overlap(new_explanation, old_explanation) >= 0.88:
            return True
        if q_sim >= 0.62 and ans_sim >= 0.62:
            return True

    return False


def parse_quiz_json(raw_output: str, source_context: str, limit: int, strict: bool = True) -> list[tuple[int, QuizQuestion]]:
    data = extract_json_from_model_output(raw_output)
    if data is None:
        return []

    if isinstance(data, dict):
        items = data.get("questions", [])
    elif isinstance(data, list):
        items = data
    else:
        return []

    if not isinstance(items, list):
        return []

    candidates: list[tuple[int, QuizQuestion]] = []

    for item in items:
        if not isinstance(item, dict):
            continue

        question = clean_quiz_part(item.get("question", ""))
        a = clean_quiz_part(item.get("option_a", item.get("A", "")))
        b = clean_quiz_part(item.get("option_b", item.get("B", "")))
        c = clean_quiz_part(item.get("option_c", item.get("C", "")))
        d = clean_quiz_part(item.get("option_d", item.get("D", "")))

        correct = normalize_correct_answer(
            item.get("correct_answer", item.get("correct", item.get("answer", item.get("correct_option", ""))))
        )
        explanation = clean_quiz_part(item.get("explanation", ""))

        if not question or not a or not b or not c or not d:
            continue
        if len(question) < 18:
            continue
        if correct not in ["A", "B", "C", "D"]:
            continue

        options = [a, b, c, d]
        if len({opt.lower() for opt in options}) < 4:
            continue
        if has_bad_option(options):
            continue

        correct_text = get_answer_text(correct, a, b, c, d)
        if not correct_text:
            continue

        wrong_options = [opt for opt in options if opt.lower() != correct_text.lower()]
        if has_partial_correct_option(correct_text, wrong_options):
            continue

        if contains_bad_source_reference(f"{question} {correct_text} {explanation}"):
            continue

        support = source_support_score(correct_text, source_context)
        if strict and support < 0.03:
            continue

        explanation = normalize_explanation_for_answer(correct_text, explanation, source_context)

        quiz_question = QuizQuestion(
            question=question,
            option_a=a,
            option_b=b,
            option_c=c,
            option_d=d,
            correct_answer=correct,
            correct_answer_text=correct_text,
            explanation=explanation,
        )

        score = question_quality_score(question, correct_text, explanation, support)
        candidates.append((score, quiz_question))

    candidates.sort(key=lambda item: item[0], reverse=True)
    return candidates[:limit]


def select_final_mixed_questions(candidates: list[tuple[int, QuizQuestion]], requested_questions: int) -> list[QuizQuestion]:
    hard_target = 3 if requested_questions >= 5 else max(1, round(requested_questions * 0.6))
    selected: list[QuizQuestion] = []

    hard_candidates = [(score, q) for score, q in candidates if is_advanced_style_question(q.question)]
    other_candidates = [(score, q) for score, q in candidates if not is_advanced_style_question(q.question)]

    for score, q in hard_candidates:
        if len([x for x in selected if is_advanced_style_question(x.question)]) >= hard_target:
            break
        if not is_duplicate_question(q, selected):
            selected.append(q)

    for score, q in other_candidates + hard_candidates:
        if len(selected) >= requested_questions:
            break
        if not is_duplicate_question(q, selected):
            selected.append(q)

    # Last-resort: return 5 instead of dropping to 3 if duplicate filter was too strict.
    for score, q in candidates:
        if len(selected) >= requested_questions:
            break
        selected.append(q)

    return selected[:requested_questions]


