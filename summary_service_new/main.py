def model_to_dict(model: Any) -> dict:
    try:
        return model.model_dump()
    except Exception:
        return model.dict()


def clean_model_output(text: str) -> str:
    if not text:
        return ""
    text = re.sub(r"<think>.*?</think>", "", text, flags=re.DOTALL | re.IGNORECASE)
    text = re.sub(r"```(?:json|text|python)?", "", text, flags=re.IGNORECASE)
    text = text.replace("```", "")
    return text.strip()


def clean_quiz_part(text: Any) -> str:
    text = str(text or "").strip()
    text = re.sub(r"^[A-Da-d]\s*[\)\.\:\-]\s*", "", text)
    text = re.sub(r"\s+", " ", text)
    return text.strip()


def normalize_difficulty(difficulty: str) -> str:
    value = (difficulty or "Medium").strip().lower()

    if value in [
        "mixed", "mix", "mixed level", "advanced", "advance", "professor",
        "professor hard", "hard advanced", "advanced hard", "level 3",
        "very hard", "expert",
    ]:
        return "Mixed"

    if value in ["hard", "h", "level 2", "difficult"]:
        return "Hard"

    return "Medium"


def display_difficulty(difficulty: str) -> str:
    return normalize_difficulty(difficulty)


def clamp_question_count(value: Any) -> int:
    try:
        value = int(value)
    except Exception:
        value = 5
    return max(1, min(value, MAX_QUIZ_QUESTIONS))


def is_pdf_file(uploaded_file: UploadFile) -> bool:
    filename = (uploaded_file.filename or "").lower()
    content_type = (uploaded_file.content_type or "").lower()
    return content_type == "application/pdf" or filename.endswith(".pdf")


def is_txt_file(uploaded_file: UploadFile) -> bool:
    filename = (uploaded_file.filename or "").lower()
    content_type = (uploaded_file.content_type or "").lower()
    return content_type.startswith("text/plain") or filename.endswith(".txt") or filename.endswith(".text")


def decode_txt_bytes(file_bytes: bytes) -> str:
    if not file_bytes:
        return ""

    for encoding in ["utf-8", "utf-8-sig", "latin-1"]:
        try:
            return file_bytes.decode(encoding)
        except UnicodeDecodeError:
            continue

    return file_bytes.decode("latin-1", errors="ignore")


def clean_source_text(text: str) -> str:
    if not text:
        return ""

    text = clean_model_output(text)
    text = text.replace("\x00", " ")
    text = re.sub(r"[ \t]+", " ", text)

    generic_noise = [
        "copyright", "all rights reserved", "references", "bibliography", "index",
        "www.", "http://", "https://",
    ]

    cleaned_lines: list[str] = []

    for line in text.splitlines():
        line = line.strip()
        if not line:
            continue

        low = line.lower()

        if re.fullmatch(r"---\s*page\s*\d+\s*---", low, flags=re.IGNORECASE):
            cleaned_lines.append(line)
            continue

        if any(noise in low for noise in generic_noise):
            continue
        if re.fullmatch(r"\d{1,4}", line):
            continue
        if len(line) <= 2:
            continue

        cleaned_lines.append(line)

    text = "\n".join(cleaned_lines)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def extract_pdf_text(file_bytes: bytes) -> str:
    try:
        pdf_document = fitz.open("pdf", file_bytes)
        pages_text: list[str] = []

        for page_index, page in enumerate(pdf_document, start=1):
            text = page.get_text("text")
            if text and text.strip():
                pages_text.append(f"\n--- PAGE {page_index} ---\n{text.strip()}")

        pdf_document.close()
        return clean_source_text("\n\n".join(pages_text))
    except Exception as e:
        logger.exception(f"PDF text extraction failed: {e}")
        return ""


def call_ollama_generate(
    prompt: str,
    model: str,
    format_json: bool = False,
    timeout: int = OLLAMA_TIMEOUT_SECONDS,
    num_ctx: int = 3072,
    num_predict: int = 1200,
    temperature: float = 0.1,
    top_p: float = 0.85,
    repeat_penalty: float = 1.12,
) -> str:
    payload: dict[str, Any] = {
        "model": model,
        "prompt": "/no_think\n" + prompt.strip(),
        "stream": True,
        "think": False,
        "keep_alive": "10m",
        "options": {
            "temperature": temperature,
            "top_p": top_p,
            "repeat_penalty": repeat_penalty,
            "num_ctx": num_ctx,
            "num_predict": num_predict,
        },
    }

    if format_json:
        payload["format"] = "json"

    parts: list[str] = []

    try:
        with requests.post(
            f"{OLLAMA_BASE_URL}/api/generate",
            json=payload,
            stream=True,
            timeout=(20, timeout),
        ) as response:
            if response.status_code == 404:
                raise HTTPException(
                    status_code=404,
                    detail=(
                        f"Ollama model not found: {model}. Run: ollama pull {model} "
                        "or change QUIZ_MODEL/SUMMARY_MODEL to the exact name from ollama list."
                    ),
                )

            response.raise_for_status()

            for line in response.iter_lines(decode_unicode=True):
                if not line:
                    continue
                try:
                    item = json.loads(line)
                except Exception:
                    continue

                if "response" in item:
                    parts.append(item.get("response") or "")

                if item.get("done") is True:
                    break

    except HTTPException:
        raise
    except requests.exceptions.ReadTimeout as e:
        raise HTTPException(
            status_code=504,
            detail=(
                "Ollama took too long. Try fewer questions, a smaller PDF section, "
                "or restart Ollama and try again."
            ),
        ) from e
    except requests.exceptions.ConnectionError as e:
        raise HTTPException(
            status_code=503,
            detail="Cannot connect to Ollama. Make sure Ollama is running on http://127.0.0.1:11434.",
        ) from e
    except requests.exceptions.RequestException as e:
        raise HTTPException(status_code=500, detail=f"Ollama request failed: {e}") from e

    return clean_model_output("".join(parts))


